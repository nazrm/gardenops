function applyLocaleToDocument(locale: Locale): void {
  if (typeof document === "undefined") return;
  document.documentElement.lang = locale === "no" ? "no" : "en";
}

applyLocaleToDocument(currentLocale);

export function getLocale(): Locale {
  return currentLocale;
}

export function getLocaleTag(): string {
  return currentLocale === "no" ? "no-NO" : "en-US";
}

export function isLocale(value: string | null | undefined): value is Locale {
  return normalizeLocale(value) !== null;
}

export function setLocale(locale: Locale, options?: { persist?: boolean }): void {
  currentLocale = locale;
  applyLocaleToDocument(locale);
  if (options?.persist !== false) {
    try {
      localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    } catch {
      // ignore storage issues
    }
  }
  listeners.forEach((listener) => listener(locale));
}

export function subscribeLocaleChange(listener: (locale: Locale) => void): () => void {
  listeners.add(listener);
  return () => {
    listeners.delete(listener);
  };
}

export function t(key: string, params?: TranslationParams): string {
  const entry = translations[currentLocale][key] ?? translations[FALLBACK_LOCALE][key];
  if (entry === undefined) return key;
  const mergedParams: TranslationParams = { ...(params ?? {}), appName: appName() };
  if (typeof entry === "function") return entry(mergedParams);
  return entry.replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, name: string) => {
    const value = mergedParams[name];
    return value == null ? "" : String(value);
  });
}

export function localizeEnum(ns: string, value: string): string {
  return t(`enum.${ns}.${value}`) || value;
}

export function formatPlantCategoryLabel(category: string): string {
  const key = PLANT_CATEGORY_TRANSLATION_KEYS[category];
  return key ? t(key) : category;
}

export function localizeRoot(root: ParentNode = document): void {
  root.querySelectorAll<HTMLElement>("[data-i18n]").forEach((element) => {
    const key = element.dataset["i18n"];
    if (!key) return;
    element.textContent = t(key);
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-title]").forEach((element) => {
    const key = element.dataset["i18nTitle"];
    if (!key) return;
    element.title = t(key);
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset["i18nPlaceholder"];
    if (!key) return;
    if (element instanceof HTMLInputElement || element instanceof HTMLTextAreaElement) {
      element.placeholder = t(key);
    }
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset["i18nAriaLabel"];
    if (!key) return;
    element.setAttribute("aria-label", t(key));
  });
}
